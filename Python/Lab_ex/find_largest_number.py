# Определяне на най-голямото число
n = int(input("Въведете n на брой цели числа: "))
ng = 0
if n > 0:
    for i in range(n):
        print("Въведете число: ")
        y = int(input())
        if y > ng:
            ng = y
    print(f"Най-голямото число е {ng}")
else:
    print("Условието не може да бъде изпълнено")
