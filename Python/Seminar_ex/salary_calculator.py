# Напишете програма, която да подкани потребителя да въведе броя на
# часовете и тарифа за час. Да се изчисли и принтира брутното заплащане.
# Примерен вход:
# Enter Hours: 35
# Enter Rate: 2.75
# Изход:
# Pay: 96.2

hours = int(input('Въведете броя на часовете си: '))
rate = float(input('Въведете тарифата си за час: '))
pay = hours * rate
print('Заплащане: ' + str(round(pay, 2)))
