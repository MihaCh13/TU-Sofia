# Изчисляване на разход на гориво за дадено разстояние

s = int(input('Въведете изминатото от вас разстояние в км: '))
l = float(input("Въведете разхода на гориво за 100км: "))
print(f"При изминато разстояние {s} и разход на гориво {l}/100, изразходеното гориво от нас за изминатия път ще е равно на {(s * l) / 100}l")
