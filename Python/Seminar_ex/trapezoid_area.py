# Напишете програма, която чете от конзолата три числа a, b и h и пресмята
# лицето на трапец с основи a и b и височина h.
# (a + b) * h / 2
# Принтирайте резултата на екрана, като го закръглите до втория знак след десетичната запетая.

a = float(input('Въведете дължината на основата а: '))
b = float(input('Въведете дължината на основата b: '))
h = float(input('Въведете височината: '))
s = (a + b) * h / 2
print(round(s, 2))
