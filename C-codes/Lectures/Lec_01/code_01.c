// Дефиниране на функция input, която приема низ (prompt) и връща цяло число
int input (char prompt[]) {
    int result;             // Деклариране на променлива за входната стойност
    printf(prompt);         // Отпечатване на подканващия текст
    scanf("%d", &result);   // Четене на цяло число от входа и запис в result
    return result;          // Връщане на резултата
}

// Функция за извеждане на температурата в Целзий и Фаренхайт
void output(int cel, float far) {
    //printf("%d C->%g F\n", cel,far);  // Алтернативен начин на извеждане
    printf("%d\t\t%g\n",cel,far);       // Отпечатване на стойностите с табулация за по-добро подравняване
}

int main() {            // Главната функция, която изпълнява програмата
    /* Kod s constanta
     const int START=0; // Деклариране на константа за начална стойност
     const int END=20;  // Деклариране на константа за крайна стойност
     const int STEP=1;  // Деклариране на константа за стъпка
*/

    //float far;            // Деклариране на променлива за температура във Фаренхайт
    int start, end, step;   // Деклариране на три променливи за начална, крайна стойност и стъпка
    int cel;                // Деклариране на променлива за температура в Целзий

    start = input ("START=");   // Въвеждане на началната стойност чрез функцията input
    end = input ("END=");       // Въвеждане на крайната стойност чрез функцията input
    do {
        step = input ("STEP=");     // Въвеждане на стъпката чрез функцията input
    } while (step <= 0);            // Проверка дали стъпката е положителна (повтаря се, докато не стане)

    //printf("Hello World\n");          // Тестване на извеждане
    printf("Cel\t\tFar\n");             // Отпечатване на заглавния ред с колони за Целзий и Фаренхайт
    printf("----------------------\n"); // Отпечатване на разделителна линия

    //for (cel=START; cel<=END; cel+=STEP)      // Използване на дефинирани константи
    for (cel = start; cel <= end; cel+=step) {  // Цикъл, който обхожда температурите от start до end с дадената стъпка
        output(cel, cel*9/5+32);                // Изчисляване на Фаренхайт и извеждане чрез output
    }

    /*
    cel=inputCel();     // Въвеждане на температура чрез inputCel
    far=cel*9/5+32;     // Преобразуване от Целзий във Фаренхайт
    output(cel,far);    // Извеждане на резултата
    */

    return 0;           // Връщане на 0 за успешното изпълнение на програмата
}

